const mongoose = require("mongoose");

const authSchema = mongoose.Schema({
  name: {
    type: String,
    required: true,
    trim: true,
  },
  email: {
    type: String,
    required: true,
    trim: true,
  },
  password: {
    type: String,
    required: true,
    trim: true,
  },
});

const UserAuth = mongoose.model("user-auth", authSchema);

module.exports = UserAuth;
